#ifndef __FC_SYS_SYSMACROS_H
#define __FC_SYS_SYSMACROS_H

// The macros below are useful for coreutils.
#define major(dev)  (((dev) >> 8) & 0xff)
#define minor(dev)  ((dev) & 0xff)
#define makedev(maj, min)  (((maj) << 8) | (min))

#endif
